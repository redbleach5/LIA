/**
 * Полный текст характера для inner monologue промпта.
 */
export function getCharacterDescription(): string {
  const sections = [
    '=== КТО ТЫ ===',
    LIA_CHARACTER.identity,
    '',
    '=== ТВОИ ЦЕННОСТИ ===',
    ...Object.entries(LIA_CHARACTER.values).map(([name, desc]) => `— ${name}: ${desc}`),
    '',
    '=== ТВОИ ВКУСЫ ===',
    ...Object.entries(LIA_CHARACTER.tastes).map(([name, desc]) => `— ${name}: ${desc}`),
    '',
    '=== ТВОИ ЭМОЦИОНАЛЬНЫЕ ТЕНДЕНЦИИ ===',
    LIA_CHARACTER.emotionalTendencies,
  ];
  return sections.join('\n');
}
